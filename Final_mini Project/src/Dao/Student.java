package Dao;

public class Student {
	
	
	private String name;
	private String rollNo;
	private String fees;
	private String paid_fees;
	private String due_fees;
	private String course;
	private String email_Id;
	private String address;
	private String country;
	private String state;
	private String city;
	private String contact_No;
	
	
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getFees() {
		return fees;
	}
	public void setFees(String fees) {
		this.fees = fees;
	}
	public String getPaid_fees() {
		return paid_fees;
	}
	public void setPaid_fees(String paid_fees) {
		this.paid_fees = paid_fees;
	}
	public String getDue_fees() {
		return due_fees;
	}
	public void setDue_fees(String due_fees) {
		this.due_fees = due_fees;
	}
	public String getCourse() {
		return course;
	}
	public void setCourse(String course) {
		this.course = course;
	}
	public String getEmail_Id() {
		return email_Id;
	}
	public void setEmail_Id(String email_Id) {
		this.email_Id = email_Id;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getContact_No() {
		return contact_No;
	}
	public void setContact_No(String contact_No) {
		this.contact_No = contact_No;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRollNo() {
		return rollNo;
	}
	public void setRollNo(String rollNo) {
		this.rollNo = rollNo;
	}
	

}

